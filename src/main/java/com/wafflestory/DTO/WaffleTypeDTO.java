package com.wafflestory.DTO;

public class WaffleTypeDTO {
	
	private Long id;
	private String name;
	private int price;
	

	public WaffleTypeDTO(Long id, String name, int price) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	

}
